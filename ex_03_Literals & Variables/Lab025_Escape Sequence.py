# Escape Sequence
#\n - new line
#\t - tabnanny
#\b - one character back space

print("Deepak\nDash")
print("Deepak\tDash")
print("Deepak\bDash")

print(r"Deepak\nDash") # Here r is called Raw. It will not take the escape sequence and will print as it is.
                       # we should use r when ever we use a Directory path or file path.


