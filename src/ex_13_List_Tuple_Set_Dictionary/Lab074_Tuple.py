# tuple is a collection of items that can be of any type
# and are indexed by integers. Tuples are ordered, immutable and allow duplicates,
# ideal for fixed data.
# parenthesis - ()

new_tuple = (1, 2, 3)
print(new_tuple)
# new_tuple[0] = 12  --Tuples don't support item assignment

# with mix data type
new_tuple2 = (1, "data", True)
print(new_tuple2)

#Tuple with single element
new_tuple3 = (8,)
print(new_tuple3)




